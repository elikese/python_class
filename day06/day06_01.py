# while문은 반복횟수를 정확히 알 수 없을때 (조건이 충족될 때까지 하세요)
"""
while 조건:
    실행할 코드
    조건을 변경해주는 코드(이게 없으면 무한반복)
"""

# 가장 기본적인 형태
숫자 = 5
while 숫자 > 0:
    print(f"현재 숫자는 {숫자}")  # 실행할코드
    숫자 -= 1  # 조건을 변경해주는 코드
print("-----------------------")

# break로 탈출하는 형태
숫자 = 5
while True:
    if 숫자 == 3:
        break  # break를 밟고 while문(반복문) 탈출
    print(f"현재 숫자는 {숫자}")
    숫자 -= 1
print("-----------------------")

# flag로 탈출하는 형태
탈출플래그 = True
숫자 = 5
while 탈출플래그:
    if 숫자 == 3:
        탈출플래그 = False
        continue  # 다음 반복으로 바로 넘어감
    print(f"현재 숫자는 {숫자}")
    숫자 -= 1

숫자리스트 = []
# while문을 사용해서 1~10 숫자 중 3의 배수를 제외한 숫자들만 숫자리스트에 담아주세요
# 다 담으면 숫자리스트 출력해주세요
# continue를 써서 3의배수는 스킵, 3의 배수가 아닌경우만 코드를 실행한다
# while문 옆에 조건을 써줄건지, 내부에 if문으로 break를 한걸지,
숫자 = 0
while True:
    숫자 += 1
    if 숫자 > 10:
        break
    if 숫자 % 3 == 0:
        continue
    숫자리스트.append(숫자)
print(숫자리스트)

