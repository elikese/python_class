# items() -> dict를 (키,값)을 튜플로 묶어서 리스트비슷한것으로 만들어준다
# [(키1,값1),(키2,값2)......(키n,값n)]
# 김씨들의 핸드폰번호만 찾아봅시다

김씨들전화번호부 = {}  # 빈 dict입니다. *빈set아닙니다!

for 키값튜플 in 전화번호부.items():
    이름, 전화번호 = 키값튜플
    if 이름.startswith("김"):
        print("김씨발견~")
    if 이름[0] == "김":
        김씨들전화번호부[이름] = 전화번호
        김씨들전화번호부.update({이름: 전화번호})

print(김씨들전화번호부)

