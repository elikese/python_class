# 비밀번호 맞추기(조건추가)
# 비밀번호 3회 틀리면, 비밀번호 3회 초과입니다! -> while문 탈출
# 비밀번호를 맞추면 문이 열렸습니다! -> while문 탈출
# 매 시도마다 남은 시도횟수도 보여주세요

비밀번호 = "1234"
시도횟수 = 0
최대시도횟수 = 3
while True:
    입력값 = input("비밀번호를 입력하세요:")
    if 비밀번호 == 입력값:
        print("문이 열렸습니다")
        break
    시도횟수 += 1
    if 시도횟수 == 최대시도횟수:
        print("비밀번호 3회 초과입니다")
        break
    print(f"비밀번호 틀렸습니다 남은시도횟수:{최대시도횟수 - 시도횟수}")

# 중첩 while문
while True:
    print("메뉴: 1.시작 2.종료")
    선택 = input("메뉴(숫자)를 입력하세요 >>")
    if 선택 == "1":
        while True:
            print("메뉴: 1.1번선택 2.2번선택 3.돌아가기")
            선택 = input("메뉴(숫자)를 입력하세요 >>")
            if 선택 == "1":
                print("1번선택")
            elif 선택 == "2":
                print("2번선택")
            elif 선택 == "3":
                print("위메뉴로 돌아갑니다")
                break
    elif 선택 == "2":
        print("시스템 종료")
        break
