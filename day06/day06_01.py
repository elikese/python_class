# range(a,b) -> a 이상 b 미만의 숫자가 순서대로 담긴 리스트 비슷한 것을 만들어준다.
for 숫자 in range(1, 11):
    print(숫자, end=" ")
print()

# enumerate(리스트 or 리스트비슷한것들) -> 각각의 요소들를 (인덱스,값) 튜플형태로 쌍을 만들어준다.
이름들 = ["김씨", "박씨", "이씨"]
for 튜플 in enumerate(이름들):
    인덱스, 이름 = 튜플  # 튜플 언패킹 (0, "김씨")
    print(f"{인덱스}번 이름:{이름}", end=", ")
print()

