# 비밀번호 맞추기(조건추가)
# 비밀번호 3회 틀리면, 비밀번호 3회 초과입니다! -> while문 탈출
# 비밀번호를 맞추면 문이 열렸습니다! -> while문 탈출
# 매 시도마다 남은 시도횟수도 보여주세요

비밀번호 = "1234"
시도횟수 = 0
최대시도횟수 = 3
while True:
    입력값 = input("비밀번호를 입력하세요:")
    if 비밀번호 == 입력값:
        print("문이 열렸습니다")
        break
    시도횟수 += 1
    if 시도횟수 == 최대시도횟수:
        print("비밀번호 3회 초과입니다")
        break
    print(f"비밀번호 틀렸습니다 남은시도횟수:{최대시도횟수 - 시도횟수}")
