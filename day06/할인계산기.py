# 할인율 역산 계산기

# 아래는 일주일 매장의 매출데이터입니다.
# 전체 매출액을 역으로 계산하여 총 할인액을 구하세요
# 원가 = 할인가 * 100 / (100 - 할인율값)

할인율 = {"월": 10, "화수목": 0, "금": 20, "토일": 30}

매출데이터 = [
    {"요일": "월", "매출": 90000},  # 원가 100000, 10% 할인
    {"요일": "화", "매출": 150000},  # 원가 150000, 할인 없음
    {"요일": "금", "매출": 160000},  # 원가 200000, 20% 할인
    {"요일": "토", "매출": 210000},  # 원가 300000, 30% 할인
]


총할인액 = 0

for 데이터 in 매출데이터:
    요일 = 데이터["요일"]
    할인가 = 데이터["매출"]

    # 요일별 할인율 결정
    if 요일 == "월":
        할인율값 = 할인율["월"]
    elif 요일 in ["화", "수", "목"]:
        할인율값 = 할인율["화수목"]
    elif 요일 == "금":
        할인율값 = 할인율["금"]
    else:  # 토, 일
        할인율값 = 할인율["토일"]

    # 원가 계산 (정수 나눗셈만 사용해도 깔끔히 나옴)
    원가 = 할인가 * 100 / (100 - 할인율값)
    할인액 = 원가 - 할인가
    총할인액 += 할인액

    print(f"{요일}요일 | 원가:{원가}원 | 할인가:{할인가}원 | 할인액:{할인액}원")

print("총 할인액 =", 총할인액, "원")
