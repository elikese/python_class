# 실습) 장보기
# 마트진열대에 있는 과일 중 마음에 드는 3가지를 장바구니에 담아서 장바구니를 print해주세요
마트진열대 = [
    ["사과", "배"],
    ["바나나", "망고"],
    ["딸기", "체리"],
]
